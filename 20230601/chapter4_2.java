public class chapter4_2 {
    public static void main(String[] args) throws Exception {
        int n = 7;
        int m = 10;
        if (n % 2 == 0)
            System.out.println("nは偶数です");
        else
            System.out.println("nは奇数です");

        if (n % 2 == 0)
            System.out.println("nは偶数です");

        if (n % 2 == 1)
            System.out.println("nは奇数です");

        if (m % 2 == 0)
            System.out.println("mは偶数です");
        else
            System.out.println("mは奇数です");

        if (m % 2 == 0)
            System.out.println("mは偶数です");

        if (m % 2 == 1)
            System.out.println("mは奇数です");

    }
}
