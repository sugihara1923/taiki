public class chapter5_2 {
    public static void main(String[] args) throws Exception {
        for (int i = 0; i <= 100; i++) {
            System.out.print(i + ",");
        }
        for (int m = 0; m <= 100; m = m + 5) {
            System.out.print(m + ",");
        }
        for(int n=20;n>0;n--){
            System.out.print(n + ",");
        }
    }
}
